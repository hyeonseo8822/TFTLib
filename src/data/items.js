// Items.md 표의 컬럼 → 한글 라벨 매핑
function buildStats({
  health,
  armor,
  magicResistance,
  speed,
  attackDamage,
  abilityPower,
  dps,
  mana,
}) {
  const entries = [
    ['체력', health, '+'],
    ['방어력', armor, '+'],
    ['마법 저항력', magicResistance, '+'],
    ['공격 속도', speed, '+', '%'],
    ['공격력', attackDamage, '+', '%'],
    ['주문력', abilityPower, '+'],
    ['치명타 확률', dps, '+', '%'],
    ['마나', mana, '+'],
  ]
  return entries
    .filter(([, v]) => v != null && v !== 0)
    .map(([label, v, sign, suffix = '']) => ({
      label,
      value: `${sign}${v}${suffix}`,
    }))
}

