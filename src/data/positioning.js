// ==========================================
// 2. 챔피언 객체 생성 헬퍼
// ==========================================
function makeChampion({
  slug,
  name,
  englishName,
  cost,
  abilityName,
  abilityDescription,
  abilityDescriptionEn,
  health,
  armor,
  magicResist,
  attackDamage,
  dps,
  attackSpeed,
  mana,
  range,
  synergies,
}) {
  return {
    id: slug,
    slug,
    name,
    englishName,
    cost,
    image: getChampionImage(slug),
    ability: {
      name: abilityName,
      description: abilityDescription,
      descriptionEn: abilityDescriptionEn,
    },
    stats: {
      health,
      armor,
      magicResist,
      attackDamage,
      dps,
      attackSpeed,
      mana,
      range,
    },
    synergies: toKrSynergies(...synergies),
  }
}

